# Curation-time (NOT runtime) fetcher for the bundled logo pack (task 21,
# provider report v2 §11). Downloads favicons for the curated ticker list
# below into assets/logos/ and regenerates lib/data/market/company_logos.dart.
# Symbols whose favicon is missing or smaller than $MIN_SIZE px are skipped:
# they render the designed ticker-ring fallback instead.
#
