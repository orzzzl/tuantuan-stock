mkdir -p "$OUT_DIR"
entries=()
skipped=()

while read -r symbol domain; do
  [[ -z "$symbol" ]] && continue
  file_name="$(echo "$symbol" | tr '[:upper:].' '[:lower:]-').png"
  path="$OUT_DIR/$file_name"
  code=$(curl -sL -o "$path" -w "%{http_code}" \
    "https://www.google.com/s2/favicons?domain=$domain&sz=128")
  if [[ "$code" != "200" ]]; then
    rm -f "$path"
    skipped+=("$symbol ($domain: HTTP $code)")
    continue
  fi
  width=$(sips -g pixelWidth "$path" 2>/dev/null | awk '/pixelWidth/ {print $2}')
  if [[ -z "$width" || "$width" -lt "$MIN_SIZE" ]]; then
    rm -f "$path"
    skipped+=("$symbol ($domain: ${width:-unreadable}px)")
    continue
  fi
  entries+=("  '$symbol': 'assets/logos/$file_name',")
done <<<"$CURATED"

{
  echo "// GENERATED by tool/fetch_logos.sh — edit the curated list there and re-run."
  echo ""
  echo "/// Bundled logo asset path for [symbol], or null: unmapped symbols render"
  echo "/// the ticker-ring fallback. Logos are build-time assets on purpose — no"
  echo "/// runtime logo host exists that is reliably reachable from both mainland"
  echo "/// China and the US (provider report v2 §11)."
  echo "String? companyLogoAsset(String symbol) => companyLogoAssets[symbol];"
  echo ""
  echo "/// The full curated pack, keyed by app symbol; exposed so tests can"
  echo "/// verify every entry points at a bundled file."
  echo "const companyLogoAssets = <String, String>{"
  printf '%s\n' "${entries[@]}" | sort
  echo "};"
} >"$DART_OUT"

echo "bundled: ${#entries[@]}"
echo "skipped: ${#skipped[@]}"
printf '  %s\n' "${skipped[@]}"
